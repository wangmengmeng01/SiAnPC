<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">{{ title }}</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="账号"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="密码"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button
        :loading="loginStatus === 'pending'"
        :disabled="loginStatus === 'pending' || loginStatus === 'success'"
        type="primary"
        class="btn-login"
        :icon="loginStatus === 'success' ? 'el-icon-check': ''"
        @click.native.prevent="handleLogin"
      >{{ submitText }}</el-button>

      <div class="tips">
        <span style="margin-right:20px;">如忘记密码，请联系管理员重置密码。</span>
      </div>
      <div style="position: relative; min-height: 30px;">
        <el-alert
          v-show="loginError.length > 0"
          class="dp-alert"
          :title="loginError"
          type="error"
          center
          show-icon
          @close="loginError = ''"
        />
      </div>
    </el-form>
  </div>
</template>

<script>

export default {
  name: 'Login',
  data() {
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('密码长度不能少于6位'))
      } else {
        callback()
      }
    }
    return {
      title: process.env.VUE_APP_TITLE,
      loginForm: {
        username: '',
        password: ''
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', message: '请输入登录账号' }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      loginStatus: -1,
      passwordType: 'password',
      redirect: undefined,
      loginError: ''
    }
  },
  computed: {
    submitText() {
      const loginStatus = this.loginStatus
      if (loginStatus === 'pending') {
        return '登录中...'
      } else if (loginStatus === 'success') {
        return '登录成功，正在跳转...'
      } else {
        return '登录'
      }
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loginStatus = 'pending'
          this.$store.dispatch('user/login', this.loginForm).then(() => {
            this.$store.dispatch('app/getTeamList')
            this.$router.push({ path: '/dashboard' || '/' })
            this.loginStatus = 'success'
          }).catch((ex) => {
            this.loginStatus = 'error'
            this.loginError = ex.message
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    }
  }
}
</script>

